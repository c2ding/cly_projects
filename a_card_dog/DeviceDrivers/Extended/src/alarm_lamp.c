void alarm_lamp(int32_t sec)
{
	static int32_t s_time = 0;
	
	if (sec>0) {s_time = sec;}
	if (sec<0) {s_time = 0;}
	if(s_time-- > 0) {out_on(9);} else {out_off(9);}
}
